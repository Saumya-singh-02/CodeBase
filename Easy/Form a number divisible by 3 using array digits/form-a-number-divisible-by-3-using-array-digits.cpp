class Solution {
  public:
    int isPossible(int N, int arr[]) {
        string sum="";
        for(int i=0;i<N;i++){
            sum+=to_string(arr[i]);
        }
        int t=0;
        for(int i=0;i<sum.length();i++){
            t+=sum[i]-'0';
        }
        return t%3==0;
    }
};

